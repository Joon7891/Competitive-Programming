int lookupTable[64] =
{
    0, 1, 1, 2, 1, 2, 2, 3, 1, 2, 2, 3, 2, 3, 3, 4,
    1, 2, 2, 3, 2, 3, 3, 4, 2, 3, 3, 4, 3, 4, 4, 5,
    1, 2, 2, 3, 2, 3, 3, 4, 2, 3, 3, 4, 3, 4, 4, 5,
    2, 3, 3, 4, 3, 4, 4, 5, 3, 4, 4, 5, 4, 5, 5, 6
};

int setbits(unsigned long long a)
{
    int counter = 0;

    for (int i = 0; i < 16; ++i)
    {
        counter += lookupTable[(a >> (4 * i)) & 0x0f];
    }

    return counter;
}